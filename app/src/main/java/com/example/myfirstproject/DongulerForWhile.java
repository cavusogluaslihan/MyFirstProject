package com.example.myfirstproject;

public class DongulerForWhile {

    public static void main(String args[]){

        // 0 - 3 döngüsü: 0,1,2,3 -> 4 kere dönecek
        // 0 ile başlar.

        for (int i=0;i<4;i++){
           System.out.println("index : "+i);
           }


        // 0 - 3
        int a = 0;
        while (a<4){
            System.out.println("a değeri : " +a);
            a++;

        }


    }

}
